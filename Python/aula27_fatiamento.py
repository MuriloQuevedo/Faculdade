# Fatiamento [i:f:p][::]

variavel = "ola mundo"
print(variavel[0:])
print(variavel[2:])
print(variavel[:2])
print(variavel[0:3])
print(variavel[-5:-3])
print(variavel[0:9:1])
print(variavel[0:9:2])
print(variavel[::-1])

# len retorna a quantidade de caracteres da string
print(len(variavel))
